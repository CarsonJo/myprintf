char	*ft_strchr(char *s, int a)
{
	int		i;
	char	c;

	c = (char)a;
	i = 0;
	if (s[i] == c)
		return (&(s[i]));
	while (s[i])
	{
		if (s[i] == c)
			return (&(s[i]));
		i++;
	}
	if (s[i] == c)
		return (&(s[i]));
	return (0);
}
